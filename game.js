// Function to calculate canvas size
function calculateCanvasSize() {
    const screenWidth = window.innerWidth;

    if (screenWidth < maxCanvasWidth) {
        canvasWidth = Math.floor(screenWidth / 20) * 20;
    } else {
        canvasWidth = maxCanvasWidth;
    }

    oneBlockSize = canvasWidth / 20;
    canvasHeight = oneBlockSize * 22;

    // Set canvas dimensions
    canvas.width = canvasWidth;
    canvas.height = canvasHeight;
}

// Function to draw the walls
function drawWalls() {
    for (let i = 0; i < map.length; i++) {
        for (let j = 0; j < map[0].length; j++) {
            if (map[i][j] === 1) {
                canvasContext.fillStyle = wallColor;
                canvasContext.fillRect(
                    j * oneBlockSize,
                    i * oneBlockSize,
                    oneBlockSize,
                    oneBlockSize
                );
            }
        }
    }
}

// Function to update and redraw the canvas
function resizeAndDraw() {
    calculateCanvasSize(); // Update canvas size
    canvasContext.clearRect(0, 0, canvas.width, canvas.height); // Clear canvas
    drawWalls(); // Redraw walls
}

// Initialize canvas and draw on load
resizeAndDraw();

// Redraw canvas on window resize
window.addEventListener("resize", resizeAndDraw);
